//ISR contexts: For cards that have device or functionality specific
//data that needs to be accessed in the ISR

union accesio_pci_isr_context
{
  struct dma_isr_context
  {
      dma_addr_t dma_addr;
      void *dma_virt_addr;
      int dma_last_buffer; //last dma started to fill
      int dma_first_valid; //first buffer containing valid data
      int dma_num_slots;
      size_t dma_slot_size;
      int dma_data_discarded;
      spinlock_t dma_data_lock;
  }dma_context;
};

//Function to be called to perform actions that vary by card. Effort should
//be made to reuse functions for cards in the same family or with identical
//operations
typedef void (*register_operation_t) (void *);

//Do we want an enum for which struct to use in ISR context?
struct accesio_pci_device_descriptor
{
  __u32                         pid;
  const char                    *name;
  irq_handler_t                 isr;
  register_operation_t          init;  //called during probe
  register_operation_t          test_latched; //does card have IRQ pending? Is this still needed now that isr is function pointer?
  register_operation_t          enable; //enable IRQs
  register_operation_t          disable; //disable IRQs
  register_operation_t          clear; //clear IRQ. TODO: May not be needed since using isr_handler above now.
  bool                          has_dma;
};



